# Controle condicional com if, elif e else

x=float(input(" Digite um número:\n"))

if x > 1:
     print("O número que você digitou é maior que zero, por tanto, é positivo")

elif x == 0:
     print("O número que voce digitou é zero.")

else:
     print("O número que voce digitou é menor que zero, por tanto, é negativo")