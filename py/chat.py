
x = 5  # Certifique-se de que a variável 'x' esteja definida

if x > 0:
    print("x é positivo")
elif x == 0:
    print("x é zero")
else:
    print("x é negativo")

# Loop for para iteração em uma lista
lista = [1, 2, 3, 4, 5]  # Definição da lista

for i in lista:
    print(i)

# Loop while para repetição até a condição ser falsa
x = 5  # Redefinindo x para uso no loop while
while x > 0:
    print(x)
    x -= 1  # Decrementa x em 1 a cada iteração
